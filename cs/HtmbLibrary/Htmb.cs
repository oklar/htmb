    // Contract
    private static string Tag(string tagName)
    {
        return $"<{tagName}></{tagName}>";
    }

    private static string TagSingle(string tagName)
    {
        return $"<{tagName}>";
    }

    private static string Tag(string tagName, string attrs)
    {
        return $"<{tagName} {attrs}></{tagName}>"; // raw insertion
    }

    private static string Tag(string tagName, string attrs, params string[] content)
    {
        return $"<{tagName} {attrs}>{GetContent(content)}</{tagName}>"; // raw insertion
    }

    private static string Tag(string tagName, params string[] content)
    {
        return $"<{tagName}>{GetContent(content)}</{tagName}>"; // raw content only
    }

    private static string TagSingle(string tagName, string attrs)
    {
        return $"<{tagName} {attrs}>"; // raw insertion
    }

    private static string GetContent(params string[] content)
    {
        return string.Join("", content);
    }
